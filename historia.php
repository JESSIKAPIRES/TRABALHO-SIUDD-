    <h1> Como Começamos </h1> 

    <h2> Começou apenas como um simples idéia de um aplicativo voltado para um público específico e acabou se tornando
        uma tecnologia eficaz e simples para ajuda no encontro de pessoas desaparecidas.

        Desta forma, em conjunto com pessoas que tinham os mesmos ideáis, começou a crianção de um aplicativo e um site
        interativo, que pessoas que desejam ajudar e pessoas que desejam divulgar algum ente desaparecido, possam com
        segurança ter uma plataforma que possibilite buscas e  informações.
        Aplicativo e site que possui o intuito de ajudar familiares e amigos de desaparecidos.</h2>